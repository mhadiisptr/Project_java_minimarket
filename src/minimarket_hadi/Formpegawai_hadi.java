    public void getdata(){
        // Code Tampil Data
       DefaultTableModel tbm = new DefaultTableModel();
               //membuat nama kolom pada tabel
                  tbm.addColumn("id_pegawai");
                  tbm.addColumn("nama_pegawai");
                  tbm.addColumn("jenis_kelamin");
                  tbm.addColumn("alamat_pegawai");
                  tbm.addColumn("Bagian");
                  tbm.addColumn("kontak_pegawai");
                tabel_hadi.setModel(tbm);              
      db.koneksiDatabase();
        try {
            String query = "SELECT * FROM tbpegawai_hadi";
            Statement st = db.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery(query);
             
            while (rs.next()) {               
              tbm.addRow(new Object[]{
                rs.getString("id_pegawai"),
                rs.getString("nama_pegawai"),
                rs.getString("jenis_kelamin"),
                rs.getString("alamat_pegawai"),
                rs.getString("Bagian"),
                rs.getString("kontak_pegawai"),
            });
               tabel_hadi.setModel(tbm);
          }            
        } catch (SQLException e)
        {      
    }
 }
