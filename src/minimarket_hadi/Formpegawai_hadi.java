    private void tabel_hadiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_hadiMouseClicked

        // TODO add your handling code here:
        db.koneksiDatabase();
        String jekel;
        boolean result = false;
        if(pria_hadi.isSelected()){
            jekel = "Pria";
        }else{
            jekel = "Wanita";
        }

        databaru = false; // menampilkan data ke textboxt
        try {
            int row =tabel_hadi.getSelectedRow();
            String tabel_klik=(tabel_hadi.getModel().getValueAt(row, 0).toString());
            db.koneksiDatabase();
            String query = "SELECT * FROM tbpegawai_hadi where id_pegawai = '" + tabel_klik + "'";
            Statement st = db.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                id_pegawai_hadi.setText(rs.getString("id_pegawai"));
                nama_pegawai_hadi.setText(rs.getString("nama_pegawai"));
                if(rs.getString("jenis_kelamin").equals("Pria")){
                    pria_hadi.setSelected(true);
                }else{
                    wanita_hadi.setSelected(true);
                }
                alamat_hadi.setText(rs.getString("alamat_pegawai"));
                bagian_hadi.setText(rs.getString("Bagian"));
                kontak_pegawai_hadi.setText(rs.getString("Kontak_Pegawai"));
            }
        } catch (Exception e)
        {

        }
    }//GEN-LAST:event_tabel_hadiMouseClicked

