     public void getdata(){
        // Code Tampil Data
       DefaultTableModel tbm = new DefaultTableModel();
               //membuat nama kolom pada tabel
                  tbm.addColumn("kode_barang");
                  tbm.addColumn("nama_barang");
                  tbm.addColumn("harga_beli");
                  tbm.addColumn("harga_jual");
                  tbm.addColumn("jumlah");
                tabel_hadi.setModel(tbm);              
      db.koneksiDatabase();
        try {
            String query = "SELECT * FROM tbbarang_hadi";
            Statement st = db.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery(query);
             
            while (rs.next()) {               
              tbm.addRow(new Object[]{
                rs.getString("kode_barang"),
                rs.getString("nama_barang"),
                rs.getString("harga_beli"),
                rs.getString("harga_jual"),
                rs.getString("jumlah"),
            });
               tabel_hadi.setModel(tbm);
          }            
        } catch (SQLException e)
        {      
    }
 }
