    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        // Cari Data
        db.koneksiDatabase();
        boolean result = false;

        try {
            String query = "SELECT * FROM tbbarang_hadi where kode_barang = '" + cari_hadi.getText() + "'";
            Statement st = db.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery(query);

            while (rs.next()) {
                kode_barang_hadi.setText(rs.getString("kode_barang"));
                nama_barang_hadi.setText(rs.getString("nama_barang"));
                harga_beli_hadi.setText(rs.getString("harga_beli"));
                harga_jual_hadi.setText(rs.getString("harga_jual"));
                jumlah_hadi.setText(rs.getString("jumlah"));

            }
        }
        catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan !");

        }
    }//GEN-LAST:event_searchActionPerformed

